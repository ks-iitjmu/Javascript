console.log("Operators in JS:");
let a = 10;
let b = 4;

// arithematic operators
console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a * b =", a * b);
console.log("a / b =", a / b);
console.log("a ** b =", a ** b);
console.log("a % b =", a % b);

// increment and decrements
console.log("++a =", ++a);
console.log("a++ =", a++);
console.log("--a =", --a);
console.log("a-- =", a--);
console.log("a =", a);
console.log("a-- =", a--);

// assignment operators
let assignment = 1;
console.log("assignment =", assignment);
console.log("assignment += 5 =>", (assignment += 5));
console.log("assignment -= 2 =>", (assignment -= 2));
console.log("assignment *= 3 =>", (assignment *= 3));
console.log("assignment /= 2 =>", (assignment /= 2));
console.log("assignment %= 4 =>", (assignment %= 4));
console.log("assignment **= 2 =>", (assignment **= 2));

// comparison operators
let comp1 = 6;
let comp2 = 7;
let comp3 = "7";
console.log("comp1 == comp2:", comp1 == comp2);
console.log("comp1 != comp2:", comp1 != comp2);
console.log("comp2 === comp3:", comp2 === comp3);
console.log("comp2 !== comp3:", comp2 !== comp3);
// other common comparison operators like  > , < , >=, <=, and ? (ternary operator)

